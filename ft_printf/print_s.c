#include "ft_printf.h"

int	print_string(char *s)
{
	int	i;

	if (s == 0)
		return (write(1, "(null)", 6));
	i = 0;
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
	}
	return (i);
}
